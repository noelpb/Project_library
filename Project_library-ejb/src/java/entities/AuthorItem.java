package entities;

import java.util.List;
import java.io.Serializable;
import java.util.ArrayList;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.JoinColumn;
import javax.persistence.JoinTable;
import javax.persistence.ManyToMany;

/**
 *
 * @author sam
 */
@Entity
public class AuthorItem implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    private Long id;
    @Column(unique = true)
    private String name;
    @ManyToMany
    @JoinTable(name = "jnd_author_book", joinColumns = @JoinColumn(name = "author_fk"), inverseJoinColumns = @JoinColumn(name = "book_fk"))
    private List<BookItem> book;

    public AuthorItem() {

    }

    public AuthorItem(String name) {
        this.name = name;
        this.book = new ArrayList<>();
    }

    public List<BookItem> getBook() {
        return book;
    }

    public void setBook(List<BookItem> book) {
        this.book = book;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof AuthorItem)) {
            return false;
        }
        AuthorItem other = (AuthorItem) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "entities.AuthorItem[ id=" + id + " ]";
    }

}
